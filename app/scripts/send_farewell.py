# Опционально: если хочешь сначала задисейблить chime-in / chat events
# чтобы прощальное сообщение не перебилось ботом — оставь True.
DRY_RUN = False   # True = только напечатает в консоль, не отправит

# После отправки прощалки — снять webhook, чтобы бот перестал получать
# сообщения от Telegram. Сервис на Render продолжит работать (фронт + API),
# но бот не будет реагировать ни на что. Чтобы вернуть бота:
#   redeploy на Render → main.py снова поставит webhook автоматически
STOP_BOT_AFTER = True


# ============================================================
# Низкоуровневый отправитель — не редактируй
# ============================================================
TELEGRAM_LIMIT = 4000   # с запасом до 4096


def _split(text: str, limit: int) -> list[str]:
    if len(text) <= limit:
        return [text]
    parts: list[str] = []
    remaining = text
    while len(remaining) > limit:
        # Ищем последний \n\n или \n перед лимитом, чтобы не резать в середине строки
        cut = remaining.rfind("\n\n", 0, limit)
        if cut < limit // 2:
            cut = remaining.rfind("\n", 0, limit)
        if cut < limit // 2:
            cut = limit
        parts.append(remaining[:cut].rstrip())
        remaining = remaining[cut:].lstrip()
    if remaining:
        parts.append(remaining)
    return parts


async def main() -> None:
    logging.basicConfig(level=logging.INFO, format="%(asctime)s %(message)s")
    log = logging.getLogger("farewell")

    chat_id = settings.tg_allowed_chat_id
    if not chat_id:
        log.error("TG_ALLOWED_CHAT_ID не задан в .env / settings")
        return

    parts = _split(MESSAGE, TELEGRAM_LIMIT)
    log.info("Сообщение разбито на %d части (лимит TG: 4096)", len(parts))

    if DRY_RUN:
        for i, p in enumerate(parts, 1):
            print(f"\n========== ЧАСТЬ {i}/{len(parts)} ==========")
            print(p)
            print(f"========== длина: {len(p)} ==========\n")
        log.info("DRY_RUN=True — ничего не отправлено")
        return

    bot = Bot(token=settings.tg_bot_token)
    try:
        for i, p in enumerate(parts, 1):
            await bot.send_message(chat_id=chat_id, text=p, parse_mode=None)
            log.info("Часть %d/%d отправлена (%d символов)", i, len(parts), len(p))
            if i < len(parts):
                await asyncio.sleep(1.0)   # небольшая пауза между частями

        if STOP_BOT_AFTER:
            # Снимаем webhook → Telegram перестаёт доставлять updates боту.
            # Сервис на Render продолжает крутиться (FastAPI, market loops,
            # snake afk), но bot не реагирует на сообщения и команды.
            # Восстановить — redeploy на Render (main.py.lifespan() выполнит
            # bot.set_webhook() заново).
            await asyncio.sleep(2.0)   # чтоб финальная часть точно успела долететь
            await bot.delete_webhook(drop_pending_updates=True)
            log.info("Webhook снят. Бот глухой к сообщениям. "
                     "Чтобы вернуть — redeploy на Render.")
    finally:
        await bot.session.close()


if __name__ == "__main__":
    asyncio.run(main())
