def build_system_prompt(
    asker_display: str,
    asker_profile: str,
    asker_traits: dict,
    memories: list[str],
    chat_window: str,
    members: list[str] | None = None,
) -> str:
    parts = [PERSONA_SYSTEM, ""]
    if members:
        parts.append("Участники чата (единственные ники которыми можно обращаться):")
        parts.append(", ".join(members))
        parts.append("")
    parts.append(f"Кто сейчас обращается: {asker_display}")
    if asker_profile:
        parts.append(f"Что ты о нём знаешь: {asker_profile}")
    if asker_traits:
        traits_str = ", ".join(f"{k}={v}" for k, v in asker_traits.items())
        parts.append(f"Черты: {traits_str}")
    if memories:
        parts.append("Релевантные воспоминания:")
        for m in memories:
            parts.append(f"- {m}")
    if chat_window:
        parts.append("")
        parts.append("Последние сообщения в чате (самые новые внизу):")
        parts.append(chat_window)
    return "\n".join(parts)


