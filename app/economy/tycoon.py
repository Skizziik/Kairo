"""Casino Tycoon — build-your-own-casino idle/active hybrid.

Three-currency model:
  chips  ($T) — internal grind currency, generated by units when occupied
  cash   ($)  — mid-tier, converted from chips at the cashier (player UX)
  coins  (🪙) — main app currency, converted from cash at the bank (capped/day)
  vip    ⭐   — prestige tokens, gained only via full reset

Game tick: server runs a periodic 'tick' on every state-fetch call (idempotent,
based on last_tick_at delta). During the tick:
  • each unit accrues chips into its tray, scaled by occupancy and bot bonuses
  • cashier bot auto-converts a fraction of chips to cash
  • slot-attendant bot auto-collects trays into chips
  • staff salaries are deducted

Visitor spawning is purely visual on the frontend — server doesn't simulate
individual visitors. Instead it computes an aggregate occupancy_pct per unit
based on the current reputation + unit tier + bot bonuses, and the frontend
samples that to drive the visual show.
"""
