async def buy_unit(user_id: int, unit_key: str, cell_x: int, cell_y: int) -> dict:
    async with pool().acquire() as conn:
        async with conn.transaction():
            await _ensure_state(conn, user_id)
            cat = await conn.fetchrow("select * from tycoon_units_catalog where key = $1", unit_key)
            if cat is None:
                return {"ok": False, "error": "Unknown unit"}
            state = await conn.fetchrow("select * from tycoon_state where user_id = $1 for update", user_id)
            cap = int(state["floor_capacity"])
            # Validate cell
            grid_w = 3 if cap <= 6 else 4 if cap <= 12 else 5 if cap <= 20 else 6
            if cell_x < 0 or cell_y < 0 or cell_x >= grid_w:
                return {"ok": False, "error": "Cell out of bounds"}
            placed_count = await conn.fetchval(
                "select count(*) from tycoon_units where user_id = $1", user_id,
            )
            if int(placed_count or 0) >= cap:
                return {"ok": False, "error": f"Зал переполнен ({cap}/{cap}). Расширь этаж."}
            # Cell already taken?
            occupied = await conn.fetchval(
                "select 1 from tycoon_units where user_id = $1 and cell_x = $2 and cell_y = $3",
                user_id, cell_x, cell_y,
            )
            if occupied:
                return {"ok": False, "error": "Эта ячейка занята"}
            # Reputation gate
            rep = float(state["reputation_stars"])
            if rep < float(cat["unlock_reputation"]):
                return {"ok": False, "error": f"Нужно ⭐{cat['unlock_reputation']:.1f} (сейчас {rep:.1f})"}
            # Cost
            cost_cash  = int(cat["cost_cash"])
            cost_chips = int(cat["cost_chips"])
            if int(state["cash"]) < cost_cash or int(state["chips"]) < cost_chips:
                return {"ok": False, "error": "Не хватает $"}
            await conn.execute(
                "update tycoon_state set cash = cash - $2, chips = chips - $3, "
                "total_lifetime_cash = total_lifetime_cash + $2 where user_id = $1",
                user_id, cost_cash, cost_chips,
            )
            await conn.execute(
                "insert into tycoon_units (user_id, unit_key, cell_x, cell_y) values ($1, $2, $3, $4)",
                user_id, unit_key, cell_x, cell_y,
            )
    try:
        await _bump_mission(user_id, "units_bought", 1)
    except Exception:
        pass
    return await get_state(user_id)


async def collect_unit(user_id: int, unit_id: int) -> dict:
    """Tap a unit's tray to collect chips into wallet."""
    async with pool().acquire() as conn:
        async with conn.transaction():
            row = await conn.fetchrow(
                "select chips_in_tray from tycoon_units where id = $1 and user_id = $2 for update",
                unit_id, user_id,
            )
            if row is None:
                return {"ok": False, "error": "Юнит не найден"}
            tray = int(row["chips_in_tray"])
            if tray <= 0:
                return {"ok": True, "collected": 0}
            await conn.execute(
                "update tycoon_units set chips_in_tray = 0, last_collected_at = now() where id = $1", unit_id,
            )
            await conn.execute(
                "update tycoon_state set chips = chips + $2 where user_id = $1",
                user_id, tray,
            )
    try:
        await _bump_mission(user_id, "trays_collected", 1)
        await _bump_mission(user_id, "chips_earned",   tray)
    except Exception:
        pass
    return {"ok": True, "collected": tray}


async def collect_all(user_id: int) -> dict:
    """Sweep all unit trays into wallet in one go."""
    async with pool().acquire() as conn:
        async with conn.transaction():
            row = await conn.fetchrow(
                "select coalesce(sum(chips_in_tray), 0) as total from tycoon_units where user_id = $1", user_id,
            )
            total = int(row["total"]) if row else 0
            if total <= 0:
                return {"ok": True, "collected": 0}
            await conn.execute(
                "update tycoon_units set chips_in_tray = 0, last_collected_at = now() where user_id = $1", user_id,
            )
            await conn.execute(
                "update tycoon_state set chips = chips + $2 where user_id = $1", user_id, total,
            )
    return {"ok": True, "collected": total}


async def convert_chips_to_cash(user_id: int, amount_chips: int) -> dict:
    """Player-initiated conversion at the cashier."""
    if amount_chips <= 0:
        return {"ok": False, "error": "Положительное число"}
    async with pool().acquire() as conn:
        async with conn.transaction():
            state = await conn.fetchrow("select chips, cash from tycoon_state where user_id = $1 for update", user_id)
            if state is None or int(state["chips"]) < amount_chips:
                return {"ok": False, "error": "Не хватает чипов"}
            cash_gain = amount_chips // CHIPS_TO_CASH_RATE
            if cash_gain <= 0:
                return {"ok": False, "error": f"Минимум {CHIPS_TO_CASH_RATE} чипов"}
            spent = cash_gain * CHIPS_TO_CASH_RATE
            await conn.execute(
                "update tycoon_state set chips = chips - $2, cash = cash + $3 where user_id = $1",
                user_id, spent, cash_gain,
            )
    try:
        await _bump_mission(user_id, "cash_converted", cash_gain)
    except Exception:
        pass
    return {"ok": True, "spent_chips": spent, "cash_gained": cash_gain}


async def convert_cash_to_coins(user_id: int, amount_cash: int) -> dict:
    """Player-initiated conversion at the bank — feeds into main app coins (capped/day)."""
    if amount_cash <= 0:
        return {"ok": False, "error": "Положительное число"}
    today = datetime.now(timezone.utc).date()
    async with pool().acquire() as conn:
        async with conn.transaction():
            state = await conn.fetchrow(
                "select cash, last_bank_conv_day, bank_conv_today, prestige_count "
                "from tycoon_state where user_id = $1 for update", user_id,
            )
            if state is None or int(state["cash"]) < amount_cash:
                return {"ok": False, "error": "Не хватает $"}
            # Reset daily counter if new day
            if state["last_bank_conv_day"] != today:
                conv_today = 0
            else:
                conv_today = int(state["bank_conv_today"] or 0)
            # Prestige bonus: cap grows by 50% per prestige
            cap = int(COINS_DAILY_CAP_BASE * (1 + 0.5 * int(state["prestige_count"] or 0)))
            cap_left = max(0, cap - conv_today)
            if cap_left == 0:
                return {"ok": False, "error": f"Дневной лимит конверсии исчерпан ({cap} 🪙)"}
            coins_gain = min(cap_left, amount_cash // CASH_TO_COINS_RATE)
            if coins_gain <= 0:
                return {"ok": False, "error": f"Минимум {CASH_TO_COINS_RATE} $"}
            spent = coins_gain * CASH_TO_COINS_RATE
            await conn.execute(
                "update tycoon_state set cash = cash - $2, "
                "bank_conv_today = $3, last_bank_conv_day = $4 where user_id = $1",
                user_id, spent, conv_today + coins_gain, today,
            )
            # Credit main app coins
            new_bal_row = await conn.fetchrow(
                "update economy_users set balance = balance + $2, total_earned = total_earned + $2 "
                "where tg_id = $1 returning balance",
                user_id, coins_gain,
            )
            new_bal = int(new_bal_row["balance"]) if new_bal_row else None
            await conn.execute(
                "insert into economy_transactions (user_id, amount, kind, reason, balance_after) "
                "values ($1, $2, 'tycoon', $3, $4)",
                user_id, coins_gain, f"tycoon_bank_conv_{spent}_cash", new_bal,
            )
    try:
        await _bump_mission(user_id, "coins_converted", coins_gain)
    except Exception:
        pass
    return {"ok": True, "spent_cash": spent, "coins_gained": coins_gain, "new_balance": new_bal}


async def hire_bot(user_id: int, kind: str) -> dict:
    cfg = BOT_KINDS.get(kind)
    if cfg is None:
        return {"ok": False, "error": "Unknown bot"}
    async with pool().acquire() as conn:
        async with conn.transaction():
            state = await conn.fetchrow("select cash from tycoon_state where user_id = $1 for update", user_id)
            if state is None:
                return {"ok": False, "error": "No state"}
            count = await conn.fetchval(
                "select count(*) from tycoon_bots where user_id = $1 and kind = $2", user_id, kind,
            )
            if int(count or 0) >= int(cfg["max_count"]):
                return {"ok": False, "error": f"Максимум {cfg['max_count']}"}
            cost = int(cfg["hire_cost"])
            if int(state["cash"]) < cost:
                return {"ok": False, "error": "Не хватает $"}
            await conn.execute(
                "update tycoon_state set cash = cash - $2 where user_id = $1", user_id, cost,
            )
            await conn.execute(
                "insert into tycoon_bots (user_id, kind) values ($1, $2)", user_id, kind,
            )
    try:
        await _bump_mission(user_id, "bots_hired", 1)
    except Exception:
        pass
    return await get_state(user_id)


