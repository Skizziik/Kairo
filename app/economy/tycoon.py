async def prestige(user_id: int) -> dict:
    """Reset everything for VIP stars. Threshold = $1B lifetime cash."""
    async with pool().acquire() as conn:
        async with conn.transaction():
            state = await conn.fetchrow(
                "select * from tycoon_state where user_id = $1 for update", user_id,
            )
            if state is None or int(state["total_lifetime_cash"]) < PRESTIGE_THRESHOLD:
                return {"ok": False, "error": f"Нужно {PRESTIGE_THRESHOLD/1e9:.1f}B $ суммарно"}
            new_stars = int(math.log10(max(1, int(state["total_lifetime_cash"]) / PRESTIGE_THRESHOLD))) + 1
            await conn.execute(
                "update tycoon_state set "
                "  chips = 0, cash = 1000, "
                "  reputation_stars = 1.0, floor_capacity = $2, "
                "  vip_stars = vip_stars + $3, "
                "  prestige_count = prestige_count + 1, "
                "  total_lifetime_cash = 0, "
                "  bank_conv_today = 0, last_bank_conv_day = null, "
                "  last_tick_at = now(), updated_at = now() "
                "where user_id = $1",
                user_id, START_FLOOR_CAPACITY, new_stars,
            )
            await conn.execute("delete from tycoon_units where user_id = $1", user_id)
            await conn.execute("delete from tycoon_bots where user_id = $1", user_id)
            # Decor stays — they're player's skins
            # Re-insert starter slot
            await conn.execute(
                "insert into tycoon_units (user_id, unit_key, cell_x, cell_y) values ($1, $2, 1, 0)",
                user_id, "slot_basic",
            )
    return await get_state(user_id)
