async def _ensure_state(conn, user_id: int) -> None:
    """Create initial state row + 1 starter slot if user is brand new."""
    row = await conn.fetchrow("select user_id from tycoon_state where user_id = $1", user_id)
    if row is not None:
        return
    await conn.execute(
        "insert into tycoon_state (user_id, chips, cash, floor_capacity, reputation_stars) "
        "values ($1, 0, 1000, $2, 1.0)",
        user_id, START_FLOOR_CAPACITY,
    )
    # Starter slot at cell (1,0) — center of top row
    await conn.execute(
        "insert into tycoon_units (user_id, unit_key, cell_x, cell_y) values ($1, $2, 1, 0)",
        user_id, "slot_basic",
    )


def _bot_counts(rows: list) -> dict[str, int]:
    out: dict[str, int] = {}
    for r in rows:
        out[r["kind"]] = out.get(r["kind"], 0) + 1
    return out


def _compute_occupancy(reputation: float, unit_tier: int, manager_bonus: float) -> float:
    """0..1 occupancy estimate. Higher rep = more visitors. Higher-tier units
    need more rep to fill up — they only really shine at 4-5 stars."""
    rep_factor    = max(0.0, min(1.0, (reputation - 0.5) / 4.5))
    tier_demand   = 1.0 - 0.10 * (unit_tier - 1)   # tier 5 needs ~1.4× more rep
    base = 0.20 + 0.65 * rep_factor * tier_demand
    return max(0.0, min(0.95, base * manager_bonus))


def _chips_per_sec_for_unit(u: dict, occupancy: float, dealer_factor: float, manager_bonus: float) -> float:
    """Effective chips/sec considering occupancy + dealer (tables) + manager."""
    base = float(u["base_chips_per_sec"]) * (1.0 + 0.5 * (u["level"] - 1))
    dealer_mult = dealer_factor if u["kind"] == "table" else 1.0
    return base * occupancy * dealer_mult * manager_bonus


async def get_state(user_id: int) -> dict:
    """Read state, run offline tick, return full snapshot for the frontend."""
    async with pool().acquire() as conn:
        async with conn.transaction():
            await _ensure_state(conn, user_id)
            state = dict(await conn.fetchrow("select * from tycoon_state where user_id = $1", user_id))
            unit_rows = await conn.fetch(
                "select u.*, c.kind, c.tier, c.base_chips_per_sec, c.capacity, c.icon, c.name as unit_name "
                "from tycoon_units u join tycoon_units_catalog c on c.key = u.unit_key "
                "where u.user_id = $1 order by u.placed_at",
                user_id,
            )
            bot_rows = await conn.fetch(
                "select * from tycoon_bots where user_id = $1", user_id,
            )
            decor_rows = await conn.fetch(
                "select d.id, d.inv_id, d.slot_index, "
                "       i.id as inv_exists, i.skin_id, i.price, i.rarity, "
                "       s.full_name, s.image_url, s.rarity as cat_rarity "
                "from tycoon_decor d "
                "left join economy_inventory i on i.id = d.inv_id "
                "left join economy_skins_catalog s on s.id = i.skin_id "
                "where d.user_id = $1 order by d.slot_index",
                user_id,
            )

            now = datetime.now(timezone.utc)
            elapsed = (now - state["last_tick_at"]).total_seconds()
            elapsed = max(0.0, min(elapsed, 12 * 3600))   # cap offline at 12h

            # Bot effects
            bots = _bot_counts(bot_rows)
            n_attendants    = bots.get("slot_attendant", 0)
            has_cashier     = bots.get("cashier", 0) > 0
            n_dealers       = bots.get("dealer", 0)
            has_manager     = bots.get("manager", 0) > 0
            n_cleaners      = bots.get("cleaner", 0)
            dealer_factor   = 1.0 + 0.5 * min(n_dealers, 5)         # +50% per dealer up to 5
            manager_bonus   = 1.15 if has_manager else 1.0
            cleaner_rep     = min(1.5, 0.5 * n_cleaners)
            decor_rep_bonus = 0.0
            for d in decor_rows:
                rar = d.get("cat_rarity") or d.get("rarity")
                if rar and d.get("inv_exists") is not None:
                    decor_rep_bonus += DECOR_REP_PER_RARITY.get(rar, 0.0)
            decor_rep_bonus = min(2.0, decor_rep_bonus)              # cap so single user can't grind 5⭐ via decor only

            # Reputation = 1 (base) + 0.4 per amenity + cleaner_rep + decor — capped 5
            n_amenities = sum(1 for u in unit_rows if u["kind"] == "amenity")
            reputation = 1.0 + 0.4 * n_amenities + cleaner_rep + decor_rep_bonus
            reputation = max(0.0, min(5.0, reputation))

            # Run tick: each unit accrues chips, attendants auto-collect, cashier auto-converts
            new_chips = int(state["chips"])
            new_cash  = int(state["cash"])
            occupancy_per_unit: dict[int, float] = {}
            chips_per_sec_per_unit: dict[int, float] = {}
            for u in unit_rows:
                occ = _compute_occupancy(reputation, int(u["tier"]), manager_bonus)
                cps = _chips_per_sec_for_unit(dict(u), occ, dealer_factor, manager_bonus)
                occupancy_per_unit[u["id"]]    = occ
                chips_per_sec_per_unit[u["id"]] = cps
                gen = int(cps * elapsed)
                if gen <= 0:
                    continue
                # Attendants auto-collect a fraction of generated chips into the player's wallet.
                # Otherwise chips pile up in the unit's tray for manual collection.
                if u["kind"] in ("slot", "table") and n_attendants > 0:
                    auto_pct = min(1.0, n_attendants * 0.20)         # 1 bot=20%, 5 bots=100%
                    auto_part = int(gen * auto_pct)
                    tray_part = gen - auto_part
                    new_chips += auto_part
                    if tray_part > 0:
                        await conn.execute(
                            "update tycoon_units set chips_in_tray = chips_in_tray + $2, "
                            "occupancy_pct = $3, last_collected_at = greatest(last_collected_at, $4) "
                            "where id = $1",
                            int(u["id"]), tray_part, occ * 100, now,
                        )
                    else:
                        await conn.execute(
                            "update tycoon_units set occupancy_pct = $2 where id = $1",
                            int(u["id"]), occ * 100,
                        )
                else:
                    await conn.execute(
                        "update tycoon_units set chips_in_tray = chips_in_tray + $2, occupancy_pct = $3 "
                        "where id = $1",
                        int(u["id"]), gen, occ * 100,
                    )

            # Cashier auto-converts wallet chips to cash periodically
            if has_cashier and new_chips > 200:
                conv_chips = int(new_chips * 0.50)                   # 50% per tick
                conv_cash  = conv_chips // CHIPS_TO_CASH_RATE
                if conv_cash > 0:
                    new_chips -= conv_chips
                    new_cash  += conv_cash

            # Salaries (per-hour rate × elapsed hours), paid in chips
            salary_chips = 0
            for kind, count in bots.items():
                cfg = BOT_KINDS.get(kind)
                if cfg:
                    salary_chips += int(cfg["salary_per_hr"] * count * (elapsed / 3600.0))
            if salary_chips > 0:
                if new_chips >= salary_chips:
                    new_chips -= salary_chips
                else:
                    # Player went negative — bots quietly stop working (no -chips, but log)
                    salary_chips = new_chips
                    new_chips = 0

            # Persist state changes from tick
            await conn.execute(
                "update tycoon_state set chips = $2, cash = $3, reputation_stars = $4, last_tick_at = $5, "
                "updated_at = now() where user_id = $1",
                user_id, new_chips, new_cash, reputation, now,
            )

            # Re-fetch units after tick so chips_in_tray is current
            unit_rows = await conn.fetch(
                "select u.*, c.kind, c.tier, c.base_chips_per_sec, c.capacity, c.icon, c.name as unit_name "
                "from tycoon_units u join tycoon_units_catalog c on c.key = u.unit_key "
                "where u.user_id = $1 order by u.placed_at",
                user_id,
            )

    # Build response
    units = []
    for u in unit_rows:
        units.append({
            "id":            int(u["id"]),
            "key":           u["unit_key"],
            "name":          u["unit_name"],
            "kind":          u["kind"],
            "tier":          int(u["tier"]),
            "icon":          u["icon"],
            "level":         int(u["level"]),
            "cell_x":        int(u["cell_x"]),
            "cell_y":        int(u["cell_y"]),
            "capacity":      int(u["capacity"]),
            "chips_in_tray": int(u["chips_in_tray"]),
            "occupancy_pct": float(u.get("occupancy_pct") or 0),
            "chips_per_sec": round(chips_per_sec_per_unit.get(int(u["id"]), 0.0), 2),
        })
    catalog = await _list_catalog(reputation, _list_unit_keys_owned(unit_rows))
    bots_payload = [{"id": int(r["id"]), "kind": r["kind"], "level": int(r["level"])} for r in bot_rows]
    decor_payload = []
    for d in decor_rows:
        if d.get("inv_exists") is None:
            continue
        decor_payload.append({
            "id":        int(d["id"]),
            "inv_id":    int(d["inv_id"]),
            "slot":      int(d["slot_index"]),
            "name":      d["full_name"],
            "image_url": d["image_url"],
            "rarity":    d.get("cat_rarity") or d.get("rarity"),
            "rep_bonus": DECOR_REP_PER_RARITY.get(d.get("cat_rarity") or "", 0.0),
        })

    cap_left = max(0, COINS_DAILY_CAP_BASE - int(state.get("bank_conv_today") or 0)
                   if state.get("last_bank_conv_day") == datetime.now(timezone.utc).date() else COINS_DAILY_CAP_BASE)

    return {
        "ok":             True,
        "chips":          int(new_chips),
        "cash":           int(new_cash),
        "vip_stars":      int(state["vip_stars"]),
        "reputation":     round(reputation, 2),
        "floor_capacity": int(state["floor_capacity"]),
        "theme":          state["theme"],
        "prestige_count": int(state["prestige_count"]),
        "lifetime_cash":  int(state["total_lifetime_cash"]),
        "next_cell_cost": _next_cell_cost(int(state["floor_capacity"])),
        "max_floor":      MAX_FLOOR_CAPACITY,
        "bank_cap_left":  int(cap_left),
        "bank_cap_total": COINS_DAILY_CAP_BASE,
        "chips_to_cash_rate": CHIPS_TO_CASH_RATE,
        "cash_to_coins_rate": CASH_TO_COINS_RATE,
        "units":          units,
        "bots":           bots_payload,
        "decor":          decor_payload,
        "catalog":        catalog,
        "bot_kinds":      [
            {"key": k, **v, "owned": _bot_counts(bot_rows).get(k, 0)} for k, v in BOT_KINDS.items()
        ],
    }


def _list_unit_keys_owned(rows) -> set[str]:
    return {r["unit_key"] for r in rows}


async def _list_catalog(reputation: float, owned: set[str]) -> list[dict]:
    async with pool().acquire() as conn:
        rows = await conn.fetch("select * from tycoon_units_catalog order by tier, cost_cash")
    out = []
    for r in rows:
        out.append({
            "key":              r["key"],
            "name":             r["name"],
            "kind":             r["kind"],
            "tier":             int(r["tier"]),
            "cost_cash":        int(r["cost_cash"]),
            "cost_chips":       int(r["cost_chips"]),
            "base_chips_per_sec": float(r["base_chips_per_sec"]),
            "capacity":         int(r["capacity"]),
            "unlock_reputation": float(r["unlock_reputation"]),
            "icon":             r["icon"],
            "description":      r["description"],
            "unlocked":         reputation >= float(r["unlock_reputation"]),
        })
    return out


def _next_cell_cost(current_capacity: int) -> int:
    if current_capacity >= MAX_FLOOR_CAPACITY:
        return 0
    growth = current_capacity - START_FLOOR_CAPACITY
    return int(CELL_BUY_BASE_COST * (CELL_BUY_GROWTH ** growth))


